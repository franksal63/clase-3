package com.paco.ciclos;

public class WhileDemo {

public static void main(String[] args){
        
		//while loop
		int count = 1;
	        while (count < 11) {
        	    System.out.println("Count is: " + count);
	            count++;
        	}

		//do-while loop
		int counter = 1;
		do {
			System.out.println("Counter is: " + counter);
			counter++;
		} while (counter < 11);
    	}
}
